-- ADMIN ROLE FUNCTION
create or replace function public.is_admin() returns boolean as $$
  select exists(select 1 from public.users where user_id = auth.uid() and role = 'admin');
$$ language sql security definer;

-- USERS
create policy "Users can view own profile" on public.users for select using (auth.uid() = user_id);
create policy "Users can update own profile" on public.users for update using (auth.uid() = user_id);
create policy "Admins have full access to users" on public.users for all using (public.is_admin());

-- ADDRESSES
create policy "Users can view own addresses" on public.addresses for select using (auth.uid() = user_id);
