-- Bảng độc lập
CREATE TABLE public.users (
  user_id uuid NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  username character varying UNIQUE,
  full_name character varying,
  email character varying NOT NULL UNIQUE,
  phone_number character varying,
  role character varying DEFAULT 'customer'::character varying CHECK (role IN ('customer', 'admin')),
  status character varying DEFAULT 'active'::character varying CHECK (status IN ('active', 'banned', 'deleted')),
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now()
);

CREATE TABLE public.categories (
  category_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  category_name character varying NOT NULL,
  slug character varying NOT NULL UNIQUE,
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now()
);

CREATE TABLE public.brands (
  brand_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  brand_name character varying NOT NULL,
  description text,
  logo_url text,
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now()
);

CREATE TABLE public.coupons (
  coupon_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  code character varying NOT NULL UNIQUE,
  discount_type character varying NOT NULL CHECK (discount_type IN ('percent', 'fixed')),
  discount_value numeric NOT NULL CHECK (discount_value >= 0),
  max_discount_amount numeric CHECK (max_discount_amount IS NULL OR max_discount_amount >= 0),
  min_order_amount numeric DEFAULT 0 CHECK (min_order_amount >= 0),
  usage_limit integer CHECK (usage_limit IS NULL OR usage_limit >= 0),
  used_count integer DEFAULT 0 CHECK (used_count >= 0),
  per_user_limit integer CHECK (per_user_limit IS NULL OR per_user_limit >= 0),
  valid_from timestamp with time zone DEFAULT now(),
  valid_to timestamp with time zone,
  status character varying DEFAULT 'active'::character varying CHECK (status IN ('active', 'inactive', 'expired')),
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now()
);

-- Bảng phụ thuộc Cấp 1
CREATE TABLE public.addresses (
  address_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_id uuid NOT NULL REFERENCES public.users(user_id) ON DELETE CASCADE,
  receiver_name character varying NOT NULL,
  receiver_phone character varying NOT NULL,
  province character varying NOT NULL,
  district character varying NOT NULL,
  ward character varying NOT NULL,
  detail_address text NOT NULL,
  is_default boolean DEFAULT false,
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now()
);

CREATE TABLE public.cart (
  cart_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_id uuid NOT NULL UNIQUE REFERENCES public.users(user_id) ON DELETE CASCADE,
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now()
);

CREATE TABLE public.products (
  product_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  product_name character varying NOT NULL,
  slug character varying NOT NULL UNIQUE,
  description text,
  origin character varying,
  weight numeric CHECK (weight IS NULL OR weight >= 0),
  base_price numeric NOT NULL CHECK (base_price >= 0),
  category_id integer REFERENCES public.categories(category_id) ON DELETE SET NULL,
  brand_id integer REFERENCES public.brands(brand_id) ON DELETE SET NULL,
  thumbnail_url text,
  status character varying DEFAULT 'active'::character varying CHECK (status IN ('active', 'inactive', 'draft')),
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now()
);

-- Bảng phụ thuộc Cấp 2
CREATE TABLE public.product_variants (
  variant_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  product_id integer NOT NULL REFERENCES public.products(product_id) ON DELETE CASCADE,
  variant_name character varying NOT NULL,
  sku character varying UNIQUE,
  price numeric NOT NULL CHECK (price >= 0),
  discount_price numeric CHECK (discount_price IS NULL OR discount_price >= 0),
  weight numeric CHECK (weight IS NULL OR weight >= 0),
  stock integer DEFAULT 0 CHECK (stock >= 0),
  status character varying DEFAULT 'active'::character varying CHECK (status IN ('active', 'inactive')),
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now()
);

CREATE TABLE public.product_gallery (
  image_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  product_id integer NOT NULL REFERENCES public.products(product_id) ON DELETE CASCADE,
  image_url text NOT NULL,
  sort_order integer DEFAULT 0 CHECK (sort_order >= 0),
  created_at timestamp with time zone DEFAULT now()
);

CREATE TABLE public.orders (
  order_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_id uuid REFERENCES public.users(user_id) ON DELETE SET NULL,
  coupon_id integer REFERENCES public.coupons(coupon_id) ON DELETE SET NULL,
  order_date timestamp with time zone DEFAULT now(),
  total_amount numeric NOT NULL CHECK (total_amount >= 0),
  shipping_fee numeric DEFAULT 0 CHECK (shipping_fee >= 0),
  discount_amount numeric DEFAULT 0 CHECK (discount_amount >= 0),
  payment_method character varying NOT NULL,
  payment_status character varying DEFAULT 'pending'::character varying CHECK (payment_status IN ('pending', 'paid', 'failed', 'refunded')),
  order_status character varying DEFAULT 'processing'::character varying CHECK (order_status IN ('pending', 'processing', 'confirmed', 'shipping', 'completed', 'cancelled', 'returned')),
  receiver_name character varying NOT NULL,
  receiver_phone character varying NOT NULL,
  province character varying NOT NULL,
  district character varying NOT NULL,
  ward character varying NOT NULL,
  detail_address text NOT NULL,
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now()
);

CREATE TABLE public.reviews (
  review_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_id uuid REFERENCES public.users(user_id) ON DELETE SET NULL,
  product_id integer NOT NULL REFERENCES public.products(product_id) ON DELETE CASCADE,
  rating integer NOT NULL CHECK (rating >= 1 AND rating <= 5),
  comment text,
  status character varying DEFAULT 'pending'::character varying CHECK (status IN ('pending', 'approved', 'rejected')),
  review_date timestamp with time zone DEFAULT now(),
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now()
);

-- Bảng phụ thuộc Cấp 3
CREATE TABLE public.cart_items (
  cart_item_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  cart_id integer NOT NULL REFERENCES public.cart(cart_id) ON DELETE CASCADE,
  product_id integer NOT NULL REFERENCES public.products(product_id) ON DELETE CASCADE,
  variant_id integer REFERENCES public.product_variants(variant_id) ON DELETE CASCADE,
  quantity integer DEFAULT 1 CHECK (quantity > 0),
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now(),
  CONSTRAINT cart_items_unique_item UNIQUE (cart_id, product_id, variant_id)
);

CREATE TABLE public.order_items (
  order_item_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  order_id integer NOT NULL REFERENCES public.orders(order_id) ON DELETE CASCADE,
  product_id integer REFERENCES public.products(product_id) ON DELETE SET NULL,
  variant_id integer REFERENCES public.product_variants(variant_id) ON DELETE SET NULL,
  quantity integer NOT NULL CHECK (quantity > 0),
  price_at_purchase numeric NOT NULL CHECK (price_at_purchase >= 0),
  product_name_at_purchase character varying,
  variant_name_at_purchase character varying,
  sku_at_purchase character varying,
  created_at timestamp with time zone DEFAULT now()
);

CREATE TABLE public.payments (
  payment_id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  order_id integer NOT NULL REFERENCES public.orders(order_id),
  payment_provider character varying NOT NULL,
  transaction_code character varying,
  amount numeric NOT NULL CHECK (amount >= 0),
  status character varying DEFAULT 'pending'::character varying CHECK (status IN ('pending', 'success', 'failed', 'refunded')),
  paid_at timestamp with time zone,
  raw_response jsonb,
  created_at timestamp with time zone DEFAULT now(),
  updated_at timestamp with time zone DEFAULT now()
);

