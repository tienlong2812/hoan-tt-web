create policy "Public can view active products" on public.products for select using (status = 'active');
create policy "Public can view product variants" on public.product_variants for select using (true);
create policy "Public can view product gallery" on public.product_gallery for select using (true);
create policy "Public can view active coupons" on public.coupons for select using (status = 'active');

create policy "Admins have full access to categories" on public.categories for all using (public.is_admin());
create policy "Admins have full access to brands" on public.brands for all using (public.is_admin());
create policy "Admins have full access to products" on public.products for all using (public.is_admin());
create policy "Admins have full access to variants" on public.product_variants for all using (public.is_admin());
create policy "Admins have full access to galleries" on public.product_gallery for all using (public.is_admin());
create policy "Admins have full access to coupons" on public.coupons for all using (public.is_admin());

